struct WG1 : Wave_Gen
{
	void Render( Train & train, Powers::Writer output )  override
	{
		int16_t volt = ( train.voltage() / 12.f * PWM_Range_f );
		
		while( output )
		{
			output = fix_mul( volt, random( 0, fix_1 ) );
		}
	}
};


//    //

struct WG2 : Wave_Gen
{
	void Render( Train & train, Powers::Writer output )  override
	{
		float volt = train.voltage() / 12.f;
		osc_1.Level( volt );

		while( output )
		{
			output = osc_1.Sin();
		}
	}

	Osc osc_1 { 440, 1 };
};


